  <p class="text-center text-3xl md:text-5xl dark:text-white font-apfel pt-5">
    Anbau von Algen
  </p>

  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4 mx-auto dark:text-white"
  >
    Algenfarmen produzieren jährlich mehr als 25 Millionen Tonnen. Der weltweite
    Wert dieser Kultur übersteigt mit 6,4 Milliarden US-Dollar (Stand 2014)
    knapp den von Zitronen. Die Algenzucht hat sich seit den späten 1950er
    Jahren zu einem Wirtschaftszweig entwickelt, der nachhaltige Arbeitsplätze
    in Entwicklungs- und Schwellenländern bietet.
    <br /> <br />
    Bei dem Algenanbau, unterscheidet man zwischen offenen und geschlossenen Kultivierungssystemen.
    Beide haben ihren Zweck und ihre Vor- und Nachteile.
  </p>
  <p
    class="text-center text-2xl md:text-4xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4 mx-auto"
  >
    Offene Systeme
  </p>

  <img
    alt="Anbau "
    src="/pictures/offeneranbau.webp"
    class="w-11/12 md:w-3/4 my-10 rounded-lg mx-auto"
    width="1024"
    height="366"
  />
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4 mx-auto dark:text-white"
  >
    Offene Systeme existieren gleichermaßen für Mikro- und Makroalgen. Oftmals
    sind die offenen Systeme technisch weniger aufwendig und benötigen auf Grund
    dessen wenig Buget und Arbeitsaufwand, jedoch benötigen diese in der Regel
    eine große Fläche. Ein Beispiel für ein offenes System wäre ein Teller, Trog
    oder auch Tank am Land. Hinzu zu der Fläche, die diese Methode beanspruchen
    würde käme, dass dieses System den Umwelteinflüssen ausgesetzt wäre und so
    der Anbau recht unkontrolliert verlaufen würde. Das führt dazu, dass die
    Verwertungskette nicht ordnungsgemäß und optimal funktionieren kann, was
    wiederum dazu führt, das der eigentliche Zweck der Algen nicht erfüllt
    werden kann.
  </p>
  <p
    class="text-center text-2xl md:text-4xl dark:text-white font-apfel  w-11/12 md:w-3/4 mx-auto"
  >
    Geschlossene Systeme
  </p>
  <img
    alt="Group photo "
    src="/pictures/geschlosseneranbau.webp"
    class="w-11/12 md:w-3/4 my-10 rounded-lg mx-auto"
    width="610"
