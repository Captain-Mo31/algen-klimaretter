    klimafreundlich und biologisch abbaubar sind könnte es dazu führen, dass die
    Menschen wieder mehr Algenplastik konsumieren oder dieses dann einfach in
    die Natur entsorgen. Dadurch würde der Umwelt wieder stark geschadet werden.
  </p>
