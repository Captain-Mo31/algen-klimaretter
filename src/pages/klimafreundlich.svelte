  >
    Die Umweltbilanz von Algenprodukte noch ist aktuell noch schwer zu bewerten.
    Das liegt primär daran, dass die Forschung, an vielen Stellen, noch nicht so
    weit ist und viele Produkte sich noch in der Entwicklung befinden. So lässt
    sich die abschließende Umweltbilanz noch nicht ermitteln. Schauen wir uns
    aber doch mal an was nötig wäre um die Produkte klimafreundlich zu
    gestallten.
  </p>
  <p
    class="text-center text-2xl md:text-4xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4 mx-auto"
