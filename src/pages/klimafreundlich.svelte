  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4 mx-auto dark:text-white"
  >
    Damit Algenprodukte klimafreundlich werden ist vor allem wichtig, dass sie
    nachhaltig angebaut werden. Dazu muss zum Bespiel die eingesetzte Energie
    aus nachhaltigen Quellen gewonnen werden. Auch die „Nahrung“, welche den
    Algen zugeführt wird, muss nachhaltig hergestellt sein. Wenn zum Beispiel
    Industrieabgase verwendet werden besteht nämlich die Gefahr, dass diese
    klimaschädlichen Gase im Verlauf der Nutzung und / oder der Herstellung
    wieder freigesetzt werden.
  </p>
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4 mx-auto dark:text-white"
  >
    Ein weiterer wichtiger Punkt für die Klimaneutralität sind die Eigenschaften
    der Produkte. Wenn sich diese zum Beispiel am Ende nicht recyceln lassen,
    weil sie sich nur unter ganz bestimmten Bedingungen wieder zersetzen oder
    die Produkte zusätzlich zu den Algen noch aus anderen Materialien, wie
    konventionellem Plastik, bestehen sind diese am Ende auch nicht besser als
    Produkte aus konventionellem Plastik. Denn dann würde auch ihr Weg wieder in
    die Verbrennungsanlage oder auf die Deponie führen.
  </p>
  <p
    class="text-center text-2xl md:text-4xl dark:text-white font-apfel  w-11/12 md:w-3/4 mx-auto"
