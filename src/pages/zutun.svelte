    Den größten Punkt macht dabei die Forschung aus. Es muss noch viel Zeit,
    Geld und Energie in die Forschung gesteckt werden. Denn viele der Produkte
    sind noch lange nicht so weit sie in der breiten Masse ein zu setzen und
    dabei noch wirtschaftlich zu arbeiten. Sie werden bisher nur im Labor mit
    komplizierten Verfahren hergestellt und müssen so noch stark weiter
    entwickelt werden.
    <br />
    Ein weiterer wichtiger Punkt ist der Ausgangstoff nämlich die Algen. Die Wissenschaft
    muss neue Algensorten entwickeln, welche ohne die genannten Nachteile auskommt
    und trotzdem die gleichen Vorteile mit sich bringt. So müsste die Blütezeit verkürzt
    oder insgesamt weniger schädliche Algen entwickelt werden.
  </p>
