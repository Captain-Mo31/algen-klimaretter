    Algen den Weg ebnen. So müssen auszureichende Forschungsgelder
    bereitgestellt und die Umsetzung entsprechend subventioniert werden. Es
    müssen aber auch Beschränkungen erlassen werden um die Gefahren dieser
    Produkte zu minimieren. So müsste zum Beispiel die Einleitung von Abwässern
    reguliert werden um die Allgenblüte einzudämmen.
