<div class="shadow-xl rounded-lg w-full mx-auto bg-green-50 dark:bg-green-900">
  <p class="text-center text-3xl md:text-5xl dark:text-white font-apfel pt-5">
    Algen als nachhaltige Energie
  </p>
  <br />
  <p
    class=" text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Algen sind in den vergangenen Jahren mehr und mehr in den Fokus der Energie
    Wirtschaft gerutscht. Aus ihnen beziehungsweise mit ihrer Hilfe lassen sich
    verschiedene nachhaltige Energieträger produzieren.
  </p>
  <br />
  <p
    class="text-center text-2xl md:text-4xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4  mx-auto"
  >
    Algen-Biodiesel
  </p>
  <img
    alt="Group photo "
    src="/pictures/petrol.webp"
    class="w-11/12 md:w-3/4  my-10 rounded-lg mx-auto"
    width="640"
    height="262"
  />
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Algen speichern durch die Fotosynthese die, durch das Sonnenlicht,
    eingefangene Energie in Form von Zucker und Fetten. Diese Fette lassen sich
    dann in einem Chemischen Verfahren zu Diesel umwandeln. Der so hergestellte
    Treibstoff ist qualitativ und von seinen Eigenschaften Vergleichbar mit
    herkömmlichen Kraftstoffen vergleichbar.
  </p>
  <p
    class="text-center text-xl md:text-3xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4  mx-auto"
  >
    Vorteile
  </p>
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Die großen Vorteile von der Gewinnung aus Algen liegt in ihre Ergiebigkeit.
    Der Ertrag von Algen pro Fläche und Jahr ist bis zu zehnmal höher als zum
    Beispiel beim Raps. Zusätzlich nimmt der Algenanbau keine Landfläche ein.
    Diese können weiter für den Anbau von Nahrungsmitteln genutzt werden. Denn
    nur um 10% des Europäischen Kraftstoffverbrauchs durch Biotreibstoff zu
    decken müssen 17% der Ackerfläche genutzt werden.
  </p>
  <p
    class="text-center text-xl md:text-3xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4  mx-auto"
  >
    Nachteile / Probleme
  </p>
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Aktuell ist die Herstellung aber noch zu ineffizient und dadurch zu teuer.
    Nach der Ansicht von Experten werden noch 10-20 Jahre Entwicklung nötig sein
    bevor sich die Treibstoffproduktion aus Algen rechnet.
  </p>
  <p
    class="text-center text-2xl md:text-4xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4  mx-auto"
  >
    Algen-Biogas
  </p>
  <img
    alt="Group photo "
    src="/pictures/biogas.webp"
    class="w-11/12 md:w-3/4  my-10 rounded-lg mx-auto"
    width="640"
    height="262"
  />
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Aus Algen lässt sich Biogas gewinnen indem man die Algen genauso wie auch
    jede andere Biomasse in der Biogasanlage verarbeitet. Von den Algen ernähren
    sich Bakterien welche dadurch Methan produzieren welches man dann verbrennen
    kann.
  </p>
  <p
    class="text-center text-xl md:text-3xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4  mx-auto"
  >
    Vorteile
  </p>
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Bei dem Biogas ergeben sich genau die gleichen Vorteile wie beim Biodiesel.
  </p>
  <p
    class="text-center text-xl md:text-3xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4  mx-auto"
  >
    Nachteile / Probleme
  </p>
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Die Probleme liegen aktuell noch darin, dass die Algen eine sehr stabile
    Hülle haben welche von den Bakterien nur schlecht „geknackt“ werden kann.
    Dadurch ist die Ergiebigkeit der Algen noch nicht sehr hoch. Es muss noch
    ein verfahren gefunden werden mit welchem sich die Algen knacken lassen,
    bevor sie den Bakterien zugeführt werden.
  </p>
  <p
    class="text-center text-2xl md:text-4xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4  mx-auto"
  >
    Algen-Wasserstoff
  </p>
  <img
    alt="Group photo "
    src="/pictures/hydrogen.webp"
    class="w-11/12 md:w-3/4  my-10 rounded-lg mx-auto"
    width="640"
    height="262"
  />
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Aus Algen lässt sich auch grüner Wasserstoff gewinnen. Dafür werden die
    Algen entweder auf eine „Schwefeldiät“ gesetzt oder ihnen wird der
    Sauerstoff entzogen. Dadurch schallten diese ihren Stoffwechsel auf
    Sparflamme. Die durch die Photosynthese überschüssig erzeugte Energie wird
    dann entsorgt in dem die Algen Wasserstoff produzieren. Dieser lässt sich
    dann auffangen und speichern.
  </p>
  <p
    class="text-center text-xl md:text-3xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4  mx-auto"
  >
    Vorteile
  </p>
  <p
    class="m-5 text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Durch Algen lässt sich (aktuell noch recht theoretisch) ökologisch
    Wasserstoff aus Sonnenlicht produzieren.
  </p>
  <p
    class="text-center text-xl md:text-3xl dark:text-white font-apfel mt-3 w-11/12 md:w-3/4  mx-auto"
  >
    Nachteile / Probleme
  </p>
  <p
    class=" pb-5 m-5 text-center text-jutify  w-11/12 md:w-3/4  mx-auto dark:text-white"
  >
    Aktuelle funktionieren diese Verfahren aber nur im Labor. Es muss also noch
    viel geforscht werden um die Verfahren so gut zu machen, dass sie
    wirtschaftlich und effizient sind
  </p>
